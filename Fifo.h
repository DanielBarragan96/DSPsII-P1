/*!
	\This writes up to nbytes bytes to the FIFO
	\If the head runs in to the tail, not all bytes are written
	\The number of bytes written is returned
*/

#include "DataTypeDefinitions.h"

//
/********************************************************************************************/
/********************************************************************************************/
/********************************************************************************************/
/*!
 	\brief	 Push de la FIFO, se utiliza para ingresar el dato en el array de la FIFO
 	\param[in]  data Valor ingresado.
 	\return void
 */
void push(uint8 data);

//
/********************************************************************************************/
/********************************************************************************************/
/********************************************************************************************/
/*!
 	 \brief	 Esta funcion tiene como dato de salida el valor FIFO[0] y desplaza toda la FIFO
 	 en una posicion
 	\return uint8
 */
uint8 pop();

//
/********************************************************************************************/
/********************************************************************************************/
/********************************************************************************************/
/*!
 	 \brief	 Funcion para encender la bandera que indica si la FIFO esta llena
 	\return void
 */
void setFIFO_flag();

//
/********************************************************************************************/
/********************************************************************************************/
/********************************************************************************************/
/*!
 	 \brief	 Apaga la bandera de la FIFO
 	\return void
 */
void clearFIFO_flag();

//
/********************************************************************************************/
/********************************************************************************************/
/********************************************************************************************/
/*!
 	 \brief	 Esta funcion entrega una direccion de memoria de la primera posicion del array FIFO
 	 es utilizada exclusivamente con la funcion putString de la UART
 	\return void
 */
sint8 *getFIFO();

//
/********************************************************************************************/
/********************************************************************************************/
/********************************************************************************************/
/*!
 	 \brief	 Pone en cero el índice de la FIFO en el que nos encontramos y limpia la FIFO
 	\return void
 */
void resetContador();

//
/********************************************************************************************/
/********************************************************************************************/
/********************************************************************************************/
/*!
 	 \brief	 Esta funcion nos da el estado de la bandera de la FIFO
 	\return BooleanType
 */
BooleanType getFIFO_flag();
